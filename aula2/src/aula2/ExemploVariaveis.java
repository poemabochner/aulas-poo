package aula2;

public class ExemploVariaveis {
	public static void main(String[] args) {
		//Tipos primitivos: um tipo primitivo armazena valoras
		//String é uma classe (escrita sempre com a primeira
		//letra maiúscula)
		String nome = "Ana";
		int idade = 20;
		float altura = 1.85f;
		double peso = 89.90;
		System.out.println("Dados: " + idade + "\n" + altura + "\n" + peso);
		System.out.println(nome.toUpperCase());
	}
}
